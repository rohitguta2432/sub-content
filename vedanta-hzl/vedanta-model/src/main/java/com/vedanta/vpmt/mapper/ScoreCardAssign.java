package com.vedanta.vpmt.mapper;

public class ScoreCardAssign {

	private String employeeId;
	private String scorecardId;
	private String weight;
	private String scoreCardGroupUserId;
	
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getScorecardId() {
		return scorecardId;
	}
	public void setScorecardId(String scorecardId) {
		this.scorecardId = scorecardId;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getScoreCardGroupUserId() {
		return scoreCardGroupUserId;
	}
	public void setScoreCardGroupUserId(String scoreCardGroupUserId) {
		this.scoreCardGroupUserId = scoreCardGroupUserId;
	}
}
